package aritmetik_op;

public class aritmetik_op {

public static void main(String[] args) {
		
		float a, b, sonuc;
		
		a = 20;
		b = 7;
		
		sonuc = a+b;
		System.out.println("a+b= " + sonuc);
		
		sonuc = a-b;
		System.out.println("a-b= " + sonuc);
		
		sonuc = a*b;
		System.out.println("a*b= " + sonuc);
		
		sonuc = a/b;
		System.out.println("a/b= " + sonuc);
		
		sonuc = a%b;
		System.out.println("a%b= " + sonuc);
	}
}
